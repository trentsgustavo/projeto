    private void controleAtualizacao() {
        btnTela.setEnabled(true);
        PermissoesDAO pd = new PermissoesDAO();
  
        switch (status) {
            case 0:
                statusCampos(true);
                btnSalvar.setText(templateTitulos.getBtnSalvar());
                btnCancelar.setEnabled(true);
                TratarCampos.trataObrigatorios(tfdTela);
                TratarCampos.trataObrigatorios(tfdCampo);
                status++;
                tfdTela.requestFocus();

                break;
            case 1:

                if (TratarCampos.verificaVazios(jPanel1)) {
                    System.out.println("entrei");
                    statusCampos(true);

                    Permissoes p = new Permissoes();
                    Telas t =new Telas();
                    TelasDAO tdao = new TelasDAO();
                    DAO<Permissoes> dao = new DAO<Permissoes>();
                    p.setDescricao(tfdCampo.getText());
                    p.setId(Integer.parseInt(tfdId.getText()));
                    t = tdao.consultarID(Integer.parseInt(tfdTela.getText()));
                    p.setTelasId(t);

                    if (p.getId() == 0) {
                        dao.salvar(p);
                       new PermissoesDAO().popularTabela(tblPermissoes, title);
                    } else {
                        dao.atualizar(p);
                        new PermissoesDAO().popularTabela(tblPermissoes, title);
                    }
                    JOptionPane.showMessageDialog(null, templateTitulos.getMsgOpSalvo());
                    TratarCampos.limparCampos(jPanel1);
                    btnCancelar.setEnabled(false);
                    btnSalvar.setText(templateTitulos.getBtnNovo());
                    statusCampos(false);
                }
                status = 0;
                break;
            default:
                break;
        }
    }

