    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        int confirma = JOptionPane.showConfirmDialog(null, "Confirma exclusão?");
        PermissoesDAO ed = new PermissoesDAO();
        Permissoes p = new Permissoes();
        DAO d = new DAO();
        Object obj = tblPermissoes.getValueAt(tblPermissoes.getSelectedRow(), 0);
        String str = String.valueOf(obj);
        p = ed.consultarID(Integer.parseInt(str));
        if (confirma == JOptionPane.YES_OPTION) {
            d.excluir(p);
            JOptionPane.showMessageDialog(null, "Item excluído com sucesso!");
            new ProdutosDAO().popularTabela(tblPermissoes, title);
        } else {
            JOptionPane.showMessageDialog(null, "Erro ao excluir registro!");
        }
        new UsuarioDAO().popularTabela(tblPermissoes, title);
    }//GEN-LAST:event_btnExcluirActionPerformed

